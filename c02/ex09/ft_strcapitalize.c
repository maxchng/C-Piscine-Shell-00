char	*ft_strcapitalize(char *str)
{
	int	c;

	c = 0;
	while (str[c])
	{
		if (c == 0 && (str[c] >= 'a' && str[c] <= 'z'))
		{
				str[c] -= 32;
		}
		else if (!((str[c - 1] >= 'A' && str[c - 1] <= 'Z')
				|| (str[c - 1] >= 'a' && str[c - 1] <= 'z')
				|| (str[c - 1] >= '0' && str[c - 1] <= '9')))
		{
			if (str[c] >= 'a' && str[c] <= 'z')
			{
				str[c] -= 32;
			}
		}
		else if (str[c] >= 'A' && str[c] <= 'Z')
		{
			str[c] += 32;
		}
		c++;
	}
	return (str);
}
