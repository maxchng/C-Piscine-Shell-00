void	logic3( char core[4][4])
{
	int	i_arr;
	int	j_arr;

	i_arr = 0;
	j_arr = 0;
	while (i_arr < 4)
	{
		if (core[0][j_arr] == '1' && core[3][j_arr] == '4')
		{
			core[1][j_arr] = '2';
			core[2][j_arr] = '3';
		}
		if (core[j_arr][0] == '1' && core[j_arr][3] == '4')
		{
			core[j_arr][1] = '2';
			core[j_arr][2] = '3';
		}
		j_arr++;
		i_arr++;
	}
}
