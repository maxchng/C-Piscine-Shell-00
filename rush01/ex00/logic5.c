void	logic5(char *arr, char core[4][4])
{
	int	i_arr;
	int	j_arr;

	i_arr = 0;
	j_arr = 0;
	while (i_arr < 4)
	{
		if (arr[j_arr] == '2' && arr[j_arr + 8] == '1')
		{
			core[0][j_arr] = '3';
		}
		if (arr[j_arr + 8] == '2' && arr[j_arr] == '1')
		{
			core[3][j_arr] = '3';
		}
		j_arr = j_arr + 2;
		i_arr++;
	}
}
