void	logic4(char core[4][4])
{
	int	i_arr;
	int	j_arr;

	i_arr = 0;
	j_arr = 0;
	while (j_arr < 4)
	{
		if (core[i_arr][0] == '1' && core[i_arr][3] == '4')
		{
			core[i_arr][1] = '2';
			core[i_arr][2] = '3';
		}
		if (core[i_arr][3] == '1' && core[i_arr][0] == '4')
		{
			core[i_arr][1] = '2';
			core[i_arr][2] = '3';
		}
		j_arr++;
		i_arr++;
	}
}
