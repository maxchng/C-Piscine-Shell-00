#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*s;
	int		i;

	i = 0;
	while (src[i])
		i++;
	s = malloc(1 * (i + 1));
	if (!s)
		return (NULL);
	s[i] = '\0';
	i = 0;
	while (src[i])
	{
		s[i] = src[i];
		i++;
	}
	return (s);
}
